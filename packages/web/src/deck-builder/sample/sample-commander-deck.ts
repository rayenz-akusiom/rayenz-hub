/** Scryfall id for Atraxa, Praetors' Voice (C16 #28). */
const ATRAXA_C16_ID = 'c34ae834-775e-447a-a330-0270c227c667';

function oracleEntry(
  def: SampleCardDef,
  opts?: { scryfallId?: string | null; imageUrl?: string | null },
): CardOracle {
  return {
    scryfallId: opts?.scryfallId ?? null,
    colourIdentity: def.ci,
    colours: def.ci.length ? def.ci : null,
    typeLine: def.type,
    layout: 'normal',
    keywords: null,
    partnerWith: null,
    oracleText: null,
    printedName: null,
    flavorName: null,
    manaValue: def.mv,
    imageUrl: opts?.imageUrl ?? null,
    finishes: ['nonfoil', 'foil'],
    updatedAt: '2026-07-23T00:00:00.000Z',
  };
}

function buildCardsAndOracle(): { cards: CardInstance[]; oracle: Record<string, CardOracle> } {
  const cards: CardInstance[] = [];
  const oracle: Record<string, CardOracle> = {};

  SAMPLE_CARDS.forEach((def, i) => {
    const instanceId = `sample-${i + 1}`;
    const isCommander = def.category === 'Commander';
    const scryfallId = isCommander ? ATRAXA_C16_ID : null;
    const imageUrl = isCommander
      ? `https://cards.scryfall.io/normal/front/${ATRAXA_C16_ID[0]}/${ATRAXA_C16_ID[1]}/${ATRAXA_C16_ID}.jpg`
      : null;

    cards.push({
      instanceId,
      name: def.name,
      quantity: def.qty ?? 1,
      primaryCategory: def.category,
      categories: [def.category],
      stack: null,
      setCode: def.set,
      collectorNumber: def.cn,
      scryfallId,
      archidektCardId: null,
      foil: false,
      proxy: false,
    });

    const key = scryfallId
      ? `id:${scryfallId}`
      : `print:${def.set.toLowerCase()}:${def.cn}`;
    oracle[key] = oracleEntry(def, { scryfallId, imageUrl });
  });

  return { cards, oracle };
}

