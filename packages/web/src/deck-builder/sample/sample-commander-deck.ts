/** Main-deck card count (includedInDeck categories), for sanity checks. */
export function sampleMainDeckCardCount(doc: DeckDocument = buildSampleCommanderDocument()): number {
  const included = new Set(
    doc.categories.filter((c) => c.includedInDeck).map((c) => c.name),
  );
  return doc.cards
    .filter((c) => included.has(c.primaryCategory))
    .reduce((n, c) => n + c.quantity, 0);
}
